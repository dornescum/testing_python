import spacy as sp

nlp = sp.load("en_core_web_sm")


text = "Newcastle United's newest recruit Harvey Barnes says playing in the Magpies team is an attacker's dream."

# text

doc = nlp(text)

for token in doc:
    print(token)

for entity in doc.ents:
    print(entity.text, entity.label_)